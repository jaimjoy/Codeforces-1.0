void solve()
{
    ll n,x;cin>>n>>x;
    vi v(n);
    for(int i=0; i<n; i++) cin>>v[i];
    sort(allr(v));
    ll ans=0;
    for(int i=0, cnt=1; i<n; i++)
    {
        ll calc = v[i]*cnt;
        if(calc >= x)
        {
            ans++;
            cnt=1;
        }
        else cnt++;
    }
    cout<<ans<<endl;
}

