void solve()
{
    int a,b;cin>>a>>b;
    int ans=32;
    for(int add=0; add<31; add++)
    {
        if(b+add == 1) continue;
        
        int op = add;
        int newb = b+add;
        int fakea = a;
        while(fakea > 0) fakea/=newb, op++;
        ans = min(ans, op);
    }
    cout<<ans<<nl;
}

int main()
{
    optimize();
    ll tc;cin>>tc;
    for(int i=1; i<=tc; i++) solve();
    return 0;
}
